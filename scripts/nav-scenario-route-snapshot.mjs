#!/usr/bin/env node
// Snapshot a catalog route's decoded routeState into the navigation scenario
// library. The decode/junction logic is shared with Navigation Demo Studio so
// headless scenarios and recorded demos cannot drift.
import { readFileSync, writeFileSync } from "node:fs";
import { buildNavigationRouteSnapshot } from "./lib/navigation-route-snapshot.mjs";

const arg = process.argv[2];
const catalog = JSON.parse(readFileSync("public-data/route-catalog.json", "utf8"));
if (!arg || arg === "--list") {
  console.log(catalog.entries.map((entry) => entry.slug).join("\n"));
  process.exit(arg ? 0 : 1);
}
const rawTokenMode = arg === "--token";
const snapshotName = rawTokenMode ? process.argv[3] : arg;
const rawToken = rawTokenMode ? process.argv[4] : null;
if (!snapshotName || (rawTokenMode && !rawToken)) {
  console.error("usage: node scripts/nav-scenario-route-snapshot.mjs <slug> | --token <name> <route-token>");
  process.exit(1);
}
const entry = rawTokenMode
  ? { slug: snapshotName, name: snapshotName, route: rawToken }
  : catalog.entries.find((candidate) => candidate.slug === arg);
if (!entry) {
  console.error(`unknown catalog slug "${arg}" (use --list)`);
  process.exit(1);
}
const routeState = await buildNavigationRouteSnapshot(rawTokenMode
  ? { routeToken: rawToken, name: snapshotName }
  : { catalogSlug: entry.slug, name: entry.name });
const header =
  `// GENERATED by scripts/nav-scenario-route-snapshot.mjs from ${rawTokenMode ? "shared route token" : "catalog slug"}\n` +
  `// "${entry.slug}" (${entry.name}) on ${new Date().toISOString().slice(0, 10)}.\n` +
  (rawTokenMode ? `// Route token: ${entry.route}\n` : "") +
  "// Re-run the script after route or reviewed-junction data changes.\n";
const outPath = `packages/core/src/navigation/scenarios/routes/${entry.slug}.js`;
writeFileSync(outPath, `${header}export default ${JSON.stringify(routeState, null, 2)};\n`);
console.log(`wrote ${outPath} (${routeState.geometry.length} points, ${routeState.junctions.length} junctions)`);
